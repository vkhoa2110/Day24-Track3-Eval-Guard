from __future__ import annotations

"""Phase A: RAGAS-style production evaluation over the 50 question set."""

import json
import math
import os
import re
import sys
from dataclasses import dataclass
from typing import Any

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
from config import ANSWERS_PATH, TEST_SET_PATH

Distribution = str

EXPECTED_DISTRIBUTIONS = ("factual", "multi_hop", "adversarial")
METRICS = ("faithfulness", "answer_relevancy", "context_precision", "context_recall")

DIAGNOSTIC_TREE = {
    "faithfulness": ("LLM hallucinating", "Tighten system prompt and require citations from retrieved context"),
    "context_recall": ("Missing relevant chunks", "Improve chunking, add BM25 terms, or raise retrieval top_k"),
    "context_precision": ("Too many irrelevant chunks", "Add reranking and stronger metadata/version filters"),
    "answer_relevancy": ("Answer does not match the question", "Improve the answer prompt and ask for direct answers first"),
}


@dataclass
class RagasResult:
    question_id: int
    distribution: Distribution
    question: str
    answer: str
    contexts: list[str]
    ground_truth: str
    faithfulness: float
    answer_relevancy: float
    context_precision: float
    context_recall: float

    @property
    def avg_score(self) -> float:
        return (
            self.faithfulness
            + self.answer_relevancy
            + self.context_precision
            + self.context_recall
        ) / 4

    @property
    def worst_metric(self) -> str:
        scores = {
            "faithfulness": self.faithfulness,
            "answer_relevancy": self.answer_relevancy,
            "context_precision": self.context_precision,
            "context_recall": self.context_recall,
        }
        return min(scores, key=scores.get)


def load_test_set_50q(path: str = TEST_SET_PATH) -> list[dict]:
    """Load the 50 question test set."""
    with open(path, encoding="utf-8-sig") as f:
        return json.load(f)


def load_answers(path: str = ANSWERS_PATH) -> list[dict]:
    """Load answers generated by setup_answers.py."""
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"answers_50q.json not found at {path}. Run: python setup_answers.py"
        )
    with open(path, encoding="utf-8-sig") as f:
        return json.load(f)


def _safe_float(value: Any) -> float:
    try:
        score = float(value)
    except (TypeError, ValueError):
        return 0.0
    if math.isnan(score) or math.isinf(score):
        return 0.0
    return max(0.0, min(1.0, score))


def _tokens(text: str) -> set[str]:
    return set(re.findall(r"\w+", (text or "").lower(), flags=re.UNICODE))


def _overlap_score(left: str, right: str) -> float:
    left_tokens = _tokens(left)
    right_tokens = _tokens(right)
    if not left_tokens or not right_tokens:
        return 0.0
    return len(left_tokens & right_tokens) / len(left_tokens)


def _local_metrics(question: str, answer: str, contexts: list[str], ground_truth: str) -> dict[str, float]:
    context_text = " ".join(contexts)
    return {
        "faithfulness": _overlap_score(answer, context_text),
        "answer_relevancy": _overlap_score(question, answer),
        "context_precision": max((_overlap_score(question, ctx) for ctx in contexts), default=0.0),
        "context_recall": _overlap_score(ground_truth, context_text),
    }


def _as_contexts(value: Any) -> list[str]:
    if value is None:
        return []
    if isinstance(value, str):
        return [value]
    if isinstance(value, list):
        return [str(item) for item in value if item is not None]
    return [str(value)]


def _metric_from(raw: Any, metric: str, fallback: float) -> float:
    if isinstance(raw, dict):
        return _safe_float(raw.get(metric, fallback))
    return _safe_float(getattr(raw, metric, fallback))


def save_phase_a_report(
    results: list[RagasResult],
    clusters: dict,
    path: str = "reports/ragas_50q.json",
) -> None:
    """Save Phase A report to JSON."""
    os.makedirs(os.path.dirname(path), exist_ok=True)

    per_dist: dict[str, dict] = {}
    for dist in EXPECTED_DISTRIBUTIONS:
        subset = [r for r in results if r.distribution == dist]
        if not subset:
            per_dist[dist] = {"count": 0, **{metric: 0.0 for metric in METRICS}, "avg_score": 0.0}
            continue
        per_dist[dist] = {
            "count": len(subset),
            **{
                metric: round(sum(getattr(r, metric) for r in subset) / len(subset), 4)
                for metric in METRICS
            },
            "avg_score": round(sum(r.avg_score for r in subset) / len(subset), 4),
        }

    report = {
        "total_questions": len(results),
        "per_distribution": per_dist,
        "failure_clusters": clusters,
        "bottom_10": bottom_10(results),
    }
    with open(path, "w", encoding="utf-8") as f:
        json.dump(report, f, ensure_ascii=False, indent=2)
    print(f"Phase A report saved -> {path}")


def group_by_distribution(test_set: list[dict]) -> dict[str, list[dict]]:
    """Group questions by the three required distributions."""
    groups = {dist: [] for dist in EXPECTED_DISTRIBUTIONS}
    for item in test_set:
        dist = item.get("distribution")
        if dist in groups:
            groups[dist].append(item)
    return groups


def run_ragas_50q(answers: list[dict]) -> list[RagasResult]:
    """Run RAGAS if available, with a deterministic local fallback."""
    if not answers:
        return []

    questions = [str(a.get("question", "")) for a in answers]
    answer_texts = [str(a.get("answer", "")) for a in answers]
    contexts = [_as_contexts(a.get("contexts", [])) for a in answers]
    ground_truths = [str(a.get("ground_truth", "")) for a in answers]

    per_question: list[Any] = []
    use_ragas_api = os.getenv("USE_RAGAS_API", "").lower() in {"1", "true", "yes"}
    if use_ragas_api:
        try:
            from src.m4_eval import evaluate_ragas

            raw = evaluate_ragas(questions, answer_texts, contexts, ground_truths)
            if isinstance(raw, dict):
                per_question = list(raw.get("per_question") or [])
        except Exception as exc:
            print(f"RAGAS unavailable; using local heuristic fallback: {exc}")

    results: list[RagasResult] = []
    for idx, item in enumerate(answers):
        fallback = _local_metrics(questions[idx], answer_texts[idx], contexts[idx], ground_truths[idx])
        raw_item = per_question[idx] if idx < len(per_question) else {}
        results.append(
            RagasResult(
                question_id=int(item.get("id", item.get("question_id", idx + 1))),
                distribution=str(item.get("distribution", "unknown")),
                question=questions[idx],
                answer=answer_texts[idx],
                contexts=contexts[idx],
                ground_truth=ground_truths[idx],
                faithfulness=_metric_from(raw_item, "faithfulness", fallback["faithfulness"]),
                answer_relevancy=_metric_from(raw_item, "answer_relevancy", fallback["answer_relevancy"]),
                context_precision=_metric_from(raw_item, "context_precision", fallback["context_precision"]),
                context_recall=_metric_from(raw_item, "context_recall", fallback["context_recall"]),
            )
        )
    return results


def bottom_10(results: list[RagasResult]) -> list[dict]:
    """Return the ten lowest average-score questions with a diagnosis."""
    output: list[dict] = []
    for rank, result in enumerate(sorted(results, key=lambda r: r.avg_score)[:10], start=1):
        diagnosis, suggested_fix = DIAGNOSTIC_TREE[result.worst_metric]
        output.append(
            {
                "rank": rank,
                "question_id": result.question_id,
                "distribution": result.distribution,
                "question": result.question,
                "avg_score": round(result.avg_score, 4),
                "worst_metric": result.worst_metric,
                "diagnosis": diagnosis,
                "suggested_fix": suggested_fix,
            }
        )
    return output


def cluster_analysis(results: list[RagasResult]) -> dict:
    """Build a worst_metric x distribution matrix and summarize the dominant failure."""
    matrix = {
        metric: {dist: 0 for dist in EXPECTED_DISTRIBUTIONS}
        for metric in METRICS
    }

    for result in results:
        if result.distribution in EXPECTED_DISTRIBUTIONS:
            matrix[result.worst_metric][result.distribution] += 1

    dominant_dist = max(
        EXPECTED_DISTRIBUTIONS,
        key=lambda dist: sum(matrix[metric][dist] for metric in METRICS),
    )
    dominant_metric = max(
        METRICS,
        key=lambda metric: sum(matrix[metric][dist] for dist in EXPECTED_DISTRIBUTIONS),
    )
    diagnosis, suggested_fix = DIAGNOSTIC_TREE[dominant_metric]
    insight = (
        f"Most failures are concentrated in '{dominant_dist}'. "
        f"The dominant weak metric is '{dominant_metric}' ({diagnosis}). "
        f"Recommended next fix: {suggested_fix}."
    )

    return {
        "matrix": matrix,
        "dominant_failure_distribution": dominant_dist,
        "dominant_failure_metric": dominant_metric,
        "insight": insight,
    }


if __name__ == "__main__":
    test_set = load_test_set_50q()
    print(f"Loaded {len(test_set)} questions")

    groups = group_by_distribution(test_set)
    for dist, questions in groups.items():
        print(f"  {dist}: {len(questions)} questions")

    answers = load_answers()
    results = run_ragas_50q(answers)

    clusters = cluster_analysis(results)
    save_phase_a_report(results, clusters)

    print("\nBottom 10 worst questions:")
    for item in bottom_10(results):
        print(
            f"  #{item['rank']} [{item['distribution']}] "
            f"avg={item['avg_score']:.3f} worst={item['worst_metric']}"
        )
    print(
        f"\nDominant failure: {clusters['dominant_failure_distribution']} / "
        f"{clusters['dominant_failure_metric']}"
    )
